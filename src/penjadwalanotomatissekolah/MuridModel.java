public class MuridModel {
    private int umur;
    private String idmurid, namamurid, kelas, jeniskelamin, gambar;

    public int getUmur() {
        return umur;
    }

    public void setUmur(int umur) {
        this.umur = umur;
    }

    public String getIdmurid() {
        return idmurid;
    }

    public void setIdmurid(String idmurid) {
        this.idmurid = idmurid;
    }

    public String getNamamurid() {
        return namamurid;
    }

    public void setNamamurid(String namamurid) {
        this.namamurid = namamurid;
    }

    public String getKelas() {
        return kelas;
    }

    public void setKelas(String kelas) {
        this.kelas = kelas;
    }

    public String getJeniskelamin() {
        return jeniskelamin;
    }

    public void setJeniskelamin(String jeniskelamin) {
        this.jeniskelamin = jeniskelamin;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

}
